int	ft_isalnum(int c)
{
	int isalpha;
	int isdigit;

	isalpha = ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')) ? 1 : 0;
	isdigit = (c >= '0' && c <= '9') ? 1 : 0;
	return (isalpha || isdigit) ? 1 : 0;
}
